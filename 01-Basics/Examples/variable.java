class variable
{
	public static void main(String args[])
	{	
		int a = 24;
		System.out.println("int value is " + a);

		double d = 23.54;
		System.out.println("double value is " + d);
		
		float f = 23.54f;
		System.out.println("float value is " + f);

		char b = 'g';
		System.out.println("char value is " + b);
	}
}